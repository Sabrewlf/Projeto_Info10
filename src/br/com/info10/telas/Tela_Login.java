    public void Logar()
    {
        String sql = "select * from usuarios where login = ? and senha = ?";
        
        try {
            //Consulta o banco de dados atraves do texto digitado nos textbox
            //o sinal de interrogação será substituido pelo que for digitado nos textbox
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txt_Usuario.getText());
            pst.setString(2, txt_Password.getText());
            
            //executando a consulta(Query)
            rs = pst.executeQuery();
            
            //validando usuario e senha
            
            if (rs.next())
            {
                //Comando abaixo valida o conteudo do campo perfil da tabela usuario
                String perfil = rs.getString(6);
                
                //libera ou nao o perfil
                
                if(perfil.equals("admin"))
                {
                frm_Principal frm = new frm_Principal();
                frm.setVisible(true);
                frm_Principal.menu_Relatorio.setEnabled(true);
                frm_Principal.menu_Add_Usuario.setEnabled(true);
                frm_Principal.lbl_Usuario.setText(rs.getString(2));
                frm_Principal.lbl_Usuario.setForeground(Color.red);
                this.dispose();
                conexao.close();
                }else{
                     frm_Principal frm = new frm_Principal();
                     frm.setVisible(true);
                     this.dispose();
                    }    
            }else{
                JOptionPane.showMessageDialog(null, "Usuario e/ou Senha Inválidos(a)!");
            }
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

